# Problem 7: 10001st prime number

def is_prime(num):
    if num <= 1:
        return False
    if num == 2:
        return True
    if num % 2 == 0:
        return False
    for i in range(3, int(num ** 0.5) + 1, 2):
        if num % i == 0:
            return False
    return True

def nth_prime(n):
    count = 0
    num = 1
    while count < n:
        num += 1
        if is_prime(num):
            count += 1
    return num

prime_position = 10001

prime_result = nth_prime(prime_position)
print(f"Problem 7: The {prime_position}st prime number is: {prime_result}")
